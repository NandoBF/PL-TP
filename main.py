from lexer import lexer
from parser import parser
from semantic import analyze_semantics

def testar_codigo(fortran_code):
    print(f"---Testing code---\n{fortran_code}\n------")
    print("Tokens:")
    lexer.input(fortran_code)
    for token in lexer:
        print(token)

    print("\nSynthatic tree generated by parser:")
    result = parser.parse(fortran_code)
    if result:
        print("\n--- Syntatic analysis ---\n")
        analyze_semantics(result)
    print(result)
    print("\n")


